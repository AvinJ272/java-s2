class ConString {
    public static void main(String[] args) {
        
        String s1 = "hello";
        String s2 = "world";
        String s3 = s1.concat(s2);
        System.out.println(s3); 
        
        
        String s4 = "good" + "morning";
        System.out.println(s4); 
    }
}
